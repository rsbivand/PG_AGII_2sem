library(spatialprobit)
data(Katrina)
coordinates(Katrina.raw) <- c("long", "lat")
proj4string(Katrina.raw) <- CRS("+proj=longlat +datum=WGS84")
library(car)
Katrina.raw$f_owntype <- recode(Katrina.raw$owntype, "1='sole'; 2='local'; 3='national'", as.factor.result=TRUE)
Katrina.raw$f_sesstatus <- recode(Katrina.raw$sesstatus, "c(1,2)='lower'; c(4,5)='upper'; else='av'", as.factor.result=TRUE)
Katrina.raw$f_sizeemp <- recode(Katrina.raw$sizeemp, "1='small'; 2='av'; 3='large'", as.factor.result=TRUE)
Katrina.raw$y0_90 <- as.numeric(Katrina.raw$days < 90)
savehistory("Katrina_data.R")
